package exercicio02.entities;

public class Calculadora {

	public int Soma(int x, int y) {
		return x + y;
	}
	
	public double Soma(double x, double y) {
		return x + y;
	}
	
	public int Soma(int x, int y, int z) {
		return x + y + z;
	}
	
	public double Soma(double x, double y, double z) {
		return x + y + z;
	}
}
